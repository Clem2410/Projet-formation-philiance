            performants dont l'impact sur l'environnement
            est fort. Un récent rapport de la commission
            californienne de l'énergie avait envisagé en 2016
            le scénario de la démocratisation de la VR qui
            aurait causé une hausse de la consommation
            d'énergie du jeu vidéo de 114 %.</p><br>
