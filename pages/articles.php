        <h3>Cloud Gaming, streaming (dématérialisé)</h3><br>
        <p>Le cloud gaming, en français jeu à la demande est
            une façon de fournir une expérience de jeux aux
            joueurs peu importe l'endroit et le moment. Les
            jeux sont exécutés sur de puissants serveurs à
            distance dans des Data center qui renvoient la
            vidéo en lecture en continu aux joueurs qui
            interagissent avec les jeux via internet en temps
            réel.</p>
        Cela implique un avantage pour le joueur,
        notamment l’élimination du besoin d'avoir des
        équipements informatiques puissants. Mais cela
        présente également un inconvénient car le cloud
        gaming nécessite d'immenses data center très
        énergivores et des bandes passantes élevées.</p>
        Il existe également une autre solution, il s’agit du
        téléchargement des jeux en dématérialisé. Cela
        signifie que l’on élimine le besoin de CD
        (emballages) mais cela nécessite également des
        data center et consomme de l'électricité. En effet,
        le stockage d'un jeu sur des serveurs demande de
        l'énergie en permanence alors qu'un jeu sur CD
        est fabriqué une seule fois.</p>
        Pour le streaming ? Vaut-il mieux acheter un jeu
        en boutique ou le télécharger directement depuis
        sa console ? Selon une étude parue en 2014 (The
        Carbon Footprint of Games Distribution), tout
        dépend en réalité de la taille du jeu. L'étude a
        comparé le processus de production du support
        physique à celui du téléchargement pour
        déterminer lequel possédait le meilleur bilan
        carbone, et à partir de quel volume le jeu
        s'inversait. L'étude arrive à la conclusion que
        pour un jeu de moins de 1,3 Go le
        téléchargement en streaming est plus intéressant
        sur le plan écologique. Or, la taille moyenne des
        jeux dépasse 8 Go. En général, les jeux en boîte
        sont donc plus écologiques que les jeux
        téléchargés.</p><br>
