        <h2 id="II">II - Le problème des ressources</h2><br>
        <p id="pp">L’avenir n’est pas à l’écologie. Métaux rares, infrastructures numériques, électricité : l’industrie
            du
            jeu vidéo consomme de plus en plus de ressources à mesure qu’elle devient la première industrie
            culturelle au monde.</p><br>
        <p>La plupart des dommages provoqués
            par les équipements de jeu vidéo ne résident pas
            dans leur phase d’utilisation, mais bien dans leur
            fabrication. Avant même d’avoir été branchés,
            chaque console ou chaque ordinateur est déjà
            responsable de la majorité des impacts
            environnementaux engendrés dans leur cycle de
            vie complet. Sous la coque en plastique de
            chacun d’entre eux, ce sont plusieurs dizaines de
            matériaux qu’il a fallu extraire, traiter et
            assembler.</p><br>
        <h3>Une console a besoin de ressources</h3><br>
        <p>Dans la mesure où les ordinateurs et
            smartphones sont également des périphériques
            de jeu importants pour l’industrie vidéoludique,
            on peut assimiler largement la high-tech au jeu
            vidéo pour ce qui concerne les besoins matériels.
            Si l’on regarde à l’échelle des appareils, les
            technologies sont globalement les mêmes, et les
            matériaux utilisés très similaires en informatique
            et téléphonie, mais aussi dans certaines
            technologies bas carbone, notamment les
            éoliennes et panneaux photovoltaïques.</p>
        <p>On y trouve des métaux rares, dits “petits
            métaux” ou “métaux de spécialité”, qui sont
            souvent des sous-produits de l’exploitation des
            grands métaux (l’indium avec le zinc, le cobalt
            avec le nickel ou le cuivre). Autres éléments
            indispensables à l’informatique, les terres rares
            peuvent se trouver sous forme concentrée pour
            les lanthanides, ou plus diffuse pour l’yttrium. Ce
            sont la séparation et le raffinage de ces minerais
            de terres rares qui engendrent une des
            principales pollutions des sols dans les pays
            producteurs comme la Chine.</p><br>
        <h3>Métaux technologiques : une pénurie</h3><br>
        <p>Si la production de métaux majeurs tels que le
            cuivre, l’aluminium ou le zinc permet pour
            l’instant de répondre à la demande en métaux
            dits “technologiques”, qui sont principalement
            des sous-produits miniers et des terres rares, leur
            demande croissante pose question quant aux
            capacités de production à des horizons de temps
            plus lointains. En effet, ces métaux très utilisés en
            informatique sont très demandés par une multitude d’industries.</p><br>
        <a id="img" href="../assets/image/img_article/pyrite-pyrites-mineral-sulfide-5.webp"><img id="img" src="../assets/image/img_article/pyrite-pyrites-mineral-sulfide-5.webp"></a><br>
        <h3>L’épuisement des ressources</h3><br>
        <p>Une étude publiée par l’Agence de
            l’environnement de la maîtrise de l’énergie
            (ADEME) en septembre 2018 s’est penchée sur
            les divers impacts environnementaux des
            équipements les plus courants dans les foyers
            français. Avec le transport de chaque appareil du
            pays producteur jusqu’en France et une
            consommation électrique française, il est possible
            de déterminer les effets de chaque phase du cycle
            de vie des objets : production, distribution,
            utilisation et fin de vie.
            Pour les équipements à forte composante
            électronique, la quantité de matériaux est le
            premier facteur d’épuisement des ressources
            minérales et fossiles, comme le montre ce
            graphique où la portion bleue de chaque colonne
            correspond à l’impact de la production des seules
            matières premières. Concrètement, ce sont les
            cartes électroniques, les écrans LCD et les
            batteries qui engendrent la plus grande
            consommation de ressources minérales. Le
            transport au sein de la chaîne de production puis
            ensuite jusqu’au consommateur épuise pour sa
            part principalement des ressources fossiles, en
            l'occurrence du pétrole.</p><br>
        <a id="img" href="../assets/image/img_article/diagramme.webp"><img id="img" src="../assets/image/img_article/diagramme.webp"></a><br>
        <h3>Peu de recyclage</h3><br>
        <p>Et quand bien même ces appareils sont recyclés,
            la part des métaux et des terres rares récupérés
            est très faible, hormis pour certains métaux
            précieux spécifiques sur lesquels se concentre le
            processus de recyclages. Ce recyclage est d’autant
            plus compliqué et énergivore que la
            miniaturisation est importante.
            Retirer et séparer les fins assemblages
            métalliques des circuits imprimés est une tâche
            extrêmement complexe, et cette difficulté
            explique que les terres rares et certains métaux
            rares comme l’indium ou le tantale aient un taux
            de recyclage généralement inférieur à 1%.</p><br>
        <h2 id="III">III - Un secteur en pleine croissance</h2><br>
        <p id="pp">Aujourd'hui, le secteur du jeu vidéo
            est en pleine croissance. Il est encore en
            phase de démocratisation en France et dans
            le monde puisque le nombre de joueurs y
            sont en augmentation.</p><br>
        <h3>Le jeu vidéo traditionnel</h3><br>
        <p>Le jeu vidéo est encore en train de se
            démocratiser dans le monde. Le revenu mondial
            du jeu vidéo est estimé à 81 milliards de dollars
            américains en 2014, et à 91 milliards en 2015 soit
            le double de l'industrie cinématographique. En
            France, on constate une augmentation de 22
            pourcent en 2017 pour un chiffre d'affaires de
            près d'1 milliard d'euros. La part du smartphone
            augmente d'année en année et le nombre de
            personnes ayant acheté un jeu vidéo a dépassé le
            milliard dans le monde en 2016. Avec le
            smartphone, se développe un nouveau mode de
            jeu, le free to play (jeux auxquels on peut jouer
            gratuitement). Cette croissance ininterrompue va
            nécessairement accroître l'impact
            environnemental du jeu vidéo. Si l'on inclut ce
            type de jeu vidéo gratuit, le nombre de personnes
            ayant déjà joué à un jeu vidéo ces dernières
            années atteint 2,2 milliards. Selon deux études, la
            croissance du secteur du jeu vidéo et plus
            largement du numérique s'accompagne d'une
            multiplication d'un facteur deux trois des impacts
            du numérique en 15 ans de 2010 à 2025.
        </p><br>
        <h3>De nouvelles innovations</h3><br>
        <p>On assiste à une course à la puissance liée aux
            développements de la puissance informatique.
            Grâce aux capacités de calcul en plus élevées, les
            graphismes des jeux deviennent par exemple de
            plus en plus réalistes avec le temps et les consoles
            finissent par devenir obsolètes. Dans le même
            temps, on assiste à une course à l'innovation avec
            la réalité virtuelle par exemple, et l'apparition
            d'une multitude de gadgets, comme des gants ou
            des combinaisons haptiques ou des simulateurs
            de marche. Le développement de la VR a pris un
            certain retard sur ce que certains attendaient et
            c'est peut être tant mieux ; les casques VR non
