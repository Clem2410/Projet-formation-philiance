        <h3>Eco Conception logicielle, green programming</h3><br>
        <p>L'écoconception logicielle également désignée
            sous le terme de conception responsable du
            service numérique est une démarche qui consiste
            à appliquer la méthodologie de l'écoconception
            aux logiciels, c'est-à-dire à réduire l'impact
            environnemental de chaque étape du cycle de vie
            d'un logiciel : sa conception, sa réalisation et son
            exécution.</p>
        C'est une démarche centrée sur le code. Elle vise
        à réduire la quantité de ressources informatiques
        nécessaires au fonctionnement du logiciel en
        l'occurrence du jeu. Toutefois, elle n'est pas
        pratiquée dans les studios de développement
        puisqu'elle n'apporte pas de gain en matière de
        rentabilité : il s'agit seulement de réduire la
        consommation du jeu lors de son utilisation.</p>
        L'utilisation du jeu est l'étape où les plus grosses
        économies sont possibles, mais c'est une question
        d'argent : optimiser un logiciel pour réduire sa
        consommation prend du temps et les
        développeurs n'ont rien à y gagner.</p>
        En conclusion, les solutions mises en places par
        les entreprises restent limitées puisqu’ils sont
        davantage motivées par la recherche de profit
        que par la réelle volonté de réduire leur impact
        écologique. Néanmoins, les choses commencent
        à bouger comme on a pu le voir avec le
        mouvement “ Playing for the planet Alliance ”.</p><br>
        <h2 id="V">V - Des solutions apporté par les associations</h2><br>
        <p id="pp">Game Impact est une association qui a
            pour objectif d'encourager la transition du jeu
            vidéo vers plus de sens en créant des ponts entre
            les mondes du jeu vidéo et l'économie sociale et
            solidaire. Son objectif est d'entraîner une
            évolution positive de la société au travers du jeu
            vidéo. Les trois parties ci dessous sont des
            propositions qui étaient dans une interview sur
            game impact.</p><br>
        <h3>Le jeu vidéo pour sensibiliser à l'écologie</h3><br>
        <p>Le jeu vidéo peut servir de média pour éduquer
            le grand public à des problématiques
            environnementales précises ou plus largement à
            le sensibiliser. À l'heure actuelle, la plupart des
            jeux n'intègrent pas les problématiques
            environnementales. La nature quand elle est
            présente sert au joueur à prélever les ressources
            dont il a besoin bien souvent sans tenir compte
            de considérations écologiques. On reproduit le
            même schéma de logique de destruction de la
            nature et de pillage des ressources que dans la
            réalité sans même la possibilité d'adopter un style
            de jeu durable.</p>
        Un Exemple de jeu intégrant une problématique
        écologique : StarDew Valley. Ce jeu propose au
        joueur de récupérer une ferme en friche et de la
        rénover petit à petit en prenant en compte des
        paramètres tels que la biodiversité, la rotation des
        cultures. Au fur et à mesure, le joueur rencontre
        les habitants et noue de nouvelles relations.
        StarDew Valley peut par exemple servir à
        sensibiliser le joueur aux circuits courts ou
        encore au compostage des déchets organiques.</p><br>
        <a id="img" href="../assets/image/img_article/star2b8a1bc1-34c8-4544-aaf4-239c9581b763_ORIGINAL.webp"><img id="img" src="../assets/image/img_article/star2b8a1bc1-34c8-4544-aaf4-239c9581b763_ORIGINAL.webp"></a><br>
        <h3>Vision de l'avenir dans les jeux vidéo</h3><br>
        <p>À l'heure actuelle, la vision de l'avenir dans les
            jeux vidéo et plus largement dans la culture
            populaire est très manichéenne ; soit tout est
            blanc soit tout est noir. D'un côté, on trouve des
            utopies technologiques (solution techniciste) ou
            la technologie a réglé tous les problèmes
            miraculeusement. De l'autre, on retrouve des
            mondes posts apocalyptiques ou la civilisation
            s'est effondrée ou subsiste partiellement dans des
            univers très sombres. Pourtant, la réalité est
            rarement aussi manichéenne ; pourquoi devrait-il
            en être ainsi du jeu vidéo ? Avec un futur trop
            optimiste, on incite le joueur à ignorer les
            problèmes actuels comme le réchauffement
            climatique. Avec un futur pessimiste, on inspire
            au joueur une peur de l'avenir qui est contre
            productive puisque l'effondrement semble donc
            inéluctable ce qui conduit à une résignation.
            Parvenir à une vision du monde plus nuancée
            peut être la base d'une réflexion plus poussée
            pour le joueur et l'inviter à penser aux
            conséquences de ses actes dans le monde réel.</p><br>
        <h3>Le Solarpunk</h3><br>
        <p>Le Steampunk est à l'origine un courant littéraire
            dont les récits se déroulent dans un univers
            industriel évoquant le 19e siècle dominé par
            l'usage du charbon et de la vapeur, à une époque
            où la notion d'écologie n'existait pas. Ce thème
            est souvent repris dans les jeux vidéo souvent en
            y mêlant des éléments d'inspiration post
            apocalyptique.</p>
        C'est en s'inspirant de ce thème, mais en le
        transposant à un futur proche et en remplaçant le
        charbon et la vapeur par des énergies
        renouvelables que le Solarpunk est né. C'est un
        mouvement récent puisque la première allusion
        qu'on en trouve date de 2008 (blog Republic of
        the Bees, article From Steampunk to Solarpunk).
        En Octobre 2019, un manifeste Solarpunk est
        publié par la Solar Punk Community. Le
        SolarPunk remplace l'esthétique victorienne du
        Steampunk par l'Art Nouveau, mouvement
        largement inspiré des formes végétales. Alors que
        le Steampunk fait souvent intervenir des thèmes
        sombres, les univers Solarpunk sont quant à eux
        plus agréables et harmonieux.</p>
        L'idée, c'est en réalité de conduire le joueur à
        intégrer par le moyen du jeu vidéo de nouvelles
        perspectives, capables d'influer sur le
        développement de la société si beaucoup de gens
        les partagent. L'objectif est que le joueur
        intériorise un rapport plus vertueux avec
        l'environnement (par exemple la science-fiction
        des années 50 a inspiré certains développements
        de technologies ultérieurs). L'idée derrière tout
        ça est de permettre au joueur de projeter cet
        univers fictif dans la réalité en lui inspirant la
        possibilité d'un futur désirable au caractère auto
        réalisateur.</p><br>
        <a id="img" href="../assets/image/img_article/tumblr-na42u3ymgh1s1rw00o6-500.webp"><img id="img" src="../assets/image/img_article/tumblr-na42u3ymgh1s1rw00o6-500.webp"></a><br>
        <h2 id="VI">VI - Le rôle des particuliers</h2><br>
        <p id="pp">Les joueurs ont un rôle important à
            jouer dans la façon qu’ils ont de consommer
            les jeux vidéo. En effet, ils peuvent adopter
            des gestes simples pour réduire l’impact
            qu’ont les jeux vidéo sur l’environnement.
            On peut par exemple éviter la
            surconsommation, acheter mieux, acheter
            moins ou encore trouver des alternatives à
            l’achat.</p><br>
        <h3>Les gestes à adopter au quotidien</h3><br>
        <p>Des gestes simples des utilisateurs peuvent
            permettre de limiter l'impact environnemental
            des jeux vidéo, comme débrancher les
            équipements en dehors de l'utilisation ou limiter
            le stockage de données sur le cloud.</p>
        Une pratique à éviter est de successivement
        télécharger puis supprimer des jeux, surtout s'ils
        sont lourds. En effet, installer consomme
        beaucoup plus que jouer. Il faut également que
        les industriels prennent des mesures pour ne pas
        encourager la surconsommation du public. Ils
        doivent participer à informer le public pour
        prendre conscience que tous les usages ne se
        valent pas, et qu’il faut privilégier les plus
        vertueux. Cette sobriété peut prendre plusieurs
        formes chez le consommateur comme par
        exemple diminuer le nombre de périphériques de
        jeu et surtout leur fréquence de renouvellement.</p>
        Ils peuvent également surveiller leur
        consommation électrique et privilégier les
        périphériques les moins gourmands pour jouer. Il
        faut réduire au maximum la taille et la fréquence
        des téléchargements (privilégier le
        téléchargement de jeux prenant peu de place).
        </p><br>
        <h3>Acheter mieux, acheter moins</h3><br>
        <p>Garder ses équipements plus longtemps, plutôt
            que de culpabiliser sur le streaming, commencer
            par conserver son matériel plus longtemps (la
            production de matériel demeure bien plus
            polluante que le streaming).</p>
        Il faut prendre conscience que,
        fondamentalement, une industrie qui oblige ses
        consommateurs à changer ses équipements aussi
        fréquemment pour pouvoir jouer aux derniers
        jeux sortis n’est pas une industrie compatible
        avec le défi environnemental qui nous incombe
        est le premier pas vers une pratique du jeu vidéo
        soutenable.</p>
        La recherche de la sobriété dans le
        développement des jeux permet aux concepteurs
        de chercher l’innovation et la prise de risque.
        Prenons Zelda Breath of the Wild, un très gros
        jeu développé par Nintendo. Malgré son
        ambition démesurée au niveau du gameplay, avec
        un terrain de jeu imposant, il ne pèse qu’un peu
        plus de 13 Go, et tourne non seulement sur la peu
        puissante Switch, mais aussi sur la vieillissante
        Wii U, une console d’ancienne génération. C’est
        un titre avec un parti pris graphique fort qui, en
        imposant à ses concepteurs une économie de
        moyens, les a fait miser sur la direction artistique
        plutôt que la prouesse technique. La vision qu’à
        Nintendo sur l’industrie et des attentes de son
        public est bien plus durable que celle de certains
        de ses concurrents. L’absence de recherche de
        qualité graphique peut donc être une bonne piste
        pour diminuer l’intensité carbone du jeu vidéo.</p><br>
        <a id="img" href="../assets/image/img_article/legend-of-zelda-Breath-of-the.webp"><img id="img" src="../assets/image/img_article/legend-of-zelda-Breath-of-the.webp"></a><br>
        <h2 id="VII">VII - Un problème qui ne peut pas être réglé avec les solutions actuelles</h2><br>
        <p id="pp">A l'heure actuelle, les solutions mises en oeuvre par les entreprises ne suffisent pas et le problème
            de la pollution du numérique demeure.</p><br>
        <h3>Un impact trop faible des différentes solutions</h3><br>
        <p>Les phases les plus polluantes du cycle de vie du
            jeu vidéo reste celles de la fabrication et de la fin
            de vie.</p>
        Mais ces solutions ne font qu'atténuer une partie
        du problème. Débrancher votre console ne
        réglera pas le problème de l'exploitation
        polluante des ressources qui ont servi à sa
        confection.</p>
        Il est en fait très complexe d'évaluer l'impact
        écologique complet du jeu vidéo en raison d'un
        cycle de vie très long et d'une chaîne de
        production complexe. De plus, certains
        équipements initialement destinés à la pratique
        du jeu vidéo comme les consoles les plus récentes
        peuvent être utilisées pour d'autres activités
        comme le visionnage de vidéo en streaming par
        l'accès à Netflix par exemple.</p>
        De plus, lorsqu'on inclut le téléphone comme
        support de jeu, la question d'inclure la fabrication
        des téléphones qui ne sont pas initialement
        destinés à la pratique du jeu vidéo dans le bilan
        du secteur du jeu vidéo sur l'environnement est
        posée. De plus en plus, jeu vidéo et secteur
        numérique sont intriqués et il semble illusoire de
        penser l'un comme séparé de l'autre.
        </p><br>
        <h3>La solution miracle n'existe pas</h3><br>
        <p>Il n'y aura pas de solution magique qui viendra
            définitivement régler le problème de la pollution
            des jeux vidéo car cette pollution existe comme
            nous l'avons vu tout au long de leur cycle de vie,
            à différents niveaux. A l'heure actuelle pour aller
            vers plus de durabilité, le meilleur moyen reste
            de conjuguer plusieurs solutions qui soient de
            l'initiative des fabricants ou des consommateurs
            et de tendre vers plus de sobriété. De nouvelles
            méthodes d'extraction et de recyclage ont été
            trouvées et continueront de l'être pour peu qu'un
            effort suffisant soit concentré sur la recherche.
            C'est le plus souvent une question de profit.</p>
        Par exemple pour le recyclage des cartes
        électroniques, le processus majoritairement
        utilisé est la pyrométallurgie, un traitement
        offrant un taux de récupération jusqu’à 95
        pourcent des métaux précieux. Ce traitement
        pourrait être amélioré car les 5 pourcent restants
        comportent des résidus de métaux et le procédé
        est lui même polluants. Des acteurs du recyclage
        se sont lancés dans la recherche d'autres
        solutions pour augmenter l'efficacité de la
        pyrométallurgie et atténuer son impact sur
        l'environnement, alors que dans le même temps
        d'autres pistes sont explorées comme l'eau
        supercritique et les bactéries.</p>
        Eau supercritique : l'eau est placée dans un état
        extrême de température et de pression (500
        degrés et 250 bars) : dans cet état l'eau devient
        oxydante et dégrade les molécules organiques de
        carbone contenues dans la résine utilisée pour
        l'assemblage des composants</p>
        Bactéries : des bactéries sont capables de
        dégrader la matrice minérale et les agents
        antioxydants qu'elles génèrent pourraient être
        utilisées pour le traitement des cartes
        électroniques. Les techniques et solutions
        actuelles peuvent donc être améliorées au moyen
        de l'innovation mais il est sans doute illusoire de
        penser qu'on pourra totalement résoudre le
        problème.</p><br>
        <h3>Le green washing</h3><br>
        <p>Aujourd'hui les grandes sociétés deviennent de
            plus en plus conscientes des problématiques
            environnementales : pas en raison d'une prise de
            conscience réelle de leur responsabilité
            écologique mais plutôt par souci d'obtenir et
            d'entretenir une image d'entreprise écologique
            auprès du grand public, cela dans une logique de
            profit. Cela passe par des campagnes de green
            washing qui sont la plupart du temps davantage
            des opérations marketing plutôt que des
            solutions menant vers plus de durabilité. Des
            manifestations ont d'ailleurs eu lieu à l'intérieur
            même de Google, avec certains employés qui
            refusaient de travailler. Dans une lettre ouverte à
            la directrice financière de Google les employés
            ont fait part de quatre revendications dont le fait
            que l'entreprise devienne neutre sur le plan
            carbone d'ici 2030 et l’arrêt de la collaboration
            avec les entreprises pétrolières. Pourtant, Google
            affirme être neutre sur le plan carbone depuis
            2007 et a depuis fait une série d'autres promesses.
            En réalité, Google investit dans des programmes
            de compensation des émissions qu'elle émet, sans
            supprimer ses propres émissions : c'est un moyen
            de détourner l'attention sans s'attaquer à la racine
            du problème.</p>
        Starbucks, grande entreprise pourtant sans
        rapport avec le jeu vidéo ont trouvé en lui une
        source d'intérêt en l'intégrant à des techniques de
        greenwashing dans le but d'obtenir une image
        plus verte sans rien faire qui aille dans le sens de
        l'écologie. En 2007, Starbucks à sortir "Planet
        green game" une jeu censé sensibiliser
        l'utilisateur aux gestes éco-citoyens pour lutter
        contre le réchauffement de la planète. C'est un
        moyen de détourner l'attention des problèmes en
        culpabilisant le consommateur.</p><br>
        <a id="img" href="../assets/image/img_article/0faadea0-df74-11e9-ba14-f333697d75a1.webp"><img id="img" src="../assets/image/img_article/0faadea0-df74-11e9-ba14-f333697d75a1.webp"></a><br>
        <h2 id="VIII">VIII - Le rôle de la politique</h2><br>
        <p id="pp">Face au mépris des considération
            écologiques, la politique présente une
            alternative qui peut permettre par la
            contrainte de réduire l'empreinte écologique
            du secteur du jeu vidéo. Le temps politique
            étant en décalage avec le rythme soutenu de
            l'innovation, il faut parfois attendre plusieurs
            années pour que les lois et les
            réglementations aboutissent.</p><br>
        <h3>La réglementation</h3><br>
        <p>Les lois sont un bon moyen de réduire l'impact
            des jeux vidéo sur l'environnement. Cet impact,
            rappelons-le, est caractérisé par plusieurs
            éléments s'additionnent. Il est possible d'agir
            pour en améliorer certains. C'est vers cette fin
            que tend la garantie obligatoire de deux ans pour
            les appareils neufs et de six mois pour les
            appareils de seconde main.</p>
        Cette garantie résulte d'une directive européenne
        transposée dans le droit français et a pour
        objectif d'encourager les producteurs à fabriquer
        du matériel solide et résistant. Un équipement
        utilisé présente deux avantages notables : d'une
        part cette loi entraîne une diminution de
        l'exploitation des ressources nécessaires à la
        conception des équipements numérique et donc
        une diminution des pollutions et des risques de
        pénuries, et d'autre part cela entraîne au bout de
        quelques années une diminution sensible de la
        quantité de déchets électroniques dont le
        recyclage lorsqu'il a lieu est complexe et
        imparfait.</p>
        Cette garantie pourrait être étendue de six mois
        pour les appareils d'occasion par loi Economie
        circulaire actuellement en discussion. Cette
        même loi prévoit la mise en place d'un indice
        officiel de réparabilité qui deviendra effectif au
        1er janvier 2021. Cet indice permettra au
        consommateur d'évaluer le degré de facilité avec
        lequel l'équipement acheté peut être réparé et
        incitera donc les fabricants à produire des
        appareils plus facilement réparables et donc plus
        vertueux écologiquement. Dès 2024, l'indice de
        réparabilité deviendra un indice de durabilité
        prenant en compte la solidité et la fiabilité du
        produit.</p><br>
        <h3>Les labels</h3><br>
        <p>Souvent trop peu connus car pas encore assez
            médiatisés, les labels sont un autre moyen de
            réduire l'impact des jeux vidéo sur la planète en
            responsabilisant le consommateur. Il existe par
            exemple en France le label Ordi 3.0 dont
            l'objectif est de promouvoir un projet national de
            territoire avec l'appui de la collectivité territoriale
            pour développer une filière nationale de collecte,
            de rénovation et de redistribution du matériel
            informatique dans une démarche de qualité
            environnementale, d'économie solidaire et
            circulaire. Le manque de campagne d'information
            au sujet de ce label est néanmoins à déplorer.</p>
        D'autres labels pourraient être imaginés pour
        remédier au caractère arbitraire de descriptions
        arbitraires quant à l'état des produits d'occasion.
        Alors que la popularité de plate-forme de vente
        en ligne de produits reconditionnés comme Back
        Market augmente, la mise en place d'un label
        pourrait permettre de fidéliser des clients en
        évitant leur déception, l'achat de produits
        d'occasion plus écologique que l'achat
        d'équipement neuf.</p><br>
