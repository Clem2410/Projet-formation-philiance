        millions d'arbres sont plantés, amélioration dans
        la conception des jeux, l'emballage et le recyclage
        des appareils.</p>
        Les engagements comprennent : Pour Sony,
        l’utilisation d’une technologie économe en
        énergie, mode faible puissance pour la PS5.</p>
        Pour Microsoft, la certification de 8250.000
        consoles XBOX comme neutres en carbone.</p>
        Google Stadia financera quant à eux des
        recherches sur la manière dont l'écologie peut
        être intégré aux jeux.</p>
        Supercell compensera toute l'empreinte de leur
        communauté.</p>
        Ubisoft développera des thèmes verts dans les
        jeux et s'approvisionnera en matériaux dans des
        usines respectueuses de l'environnement
        Sports Interactive éliminera 20 tonnes
        d'emballages en passant du plastique à une
        alternative recyclée pour toutes les futures
        versions de Football Manager.</p>
        Et enfin, ZooCraft de Creative Mobile évoluera
        pour devenir un jeu axé sur des problématiques
        écologiques.</p><br>
