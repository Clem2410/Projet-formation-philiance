            <h2 id="">I - Réchauffement climatique</h2><br>
            <p id="pp">De la conception au recyclage, les différentes étapes de vie du jeu vidéo son à l'origine de
                l'émission de gaz à effet de serre qui contribuent au réchauffement climatique. Alors même que
                les dernières projections du GIEC montrent que la température de surface du globe pourrait
                croître de 1,1 à 6,4 °C supplémentaires au cours du XXIe siècle, la question de l'avenir du jeu vidéo
                est donc posée.</p><br>

            <p>Afin de lutter contre le réchauffement
                climatique, on pense d'emblée à moins
                emprunter l'avion ou la voiture, aux économies
                d'énergie ou à manger moins de viande.
                Rarement à modifier notre rapport au jeu vidéo,
                tout simplement parce que la pollution issue de
                ce secteur demeure largement invisible à nos
                yeux. Pourtant, le jeu vidéo est aujourd'hui la
                première industrie culturelle du monde et des
                millions de consoles et de jeux sont vendus
                chaque année. Le numérique a un cycle de vie
                qui se compose de cinq étapes : la conception, la
                fabrication, le transport, la phase d'utilisation et
                finalement la fin de vie. Chacune de ces étapes
                est responsable de l'émission de gaz à effet de
                serre.</p>
            <h3>Avant la vente</h3><br>
            <p>Avant même d'arriver au joueur, les équipements
                de jeu vidéo auront déjà pollué. Son industrie
                repose sur une longue chaîne de production qui
                demande d'importantes quantités d'énergie et de
                matériaux.
                Son bilan en termes d'émission de gaz à effet de
                serre est lourd. Les jeux vidéo n'apparaissent pas
                ex nihilo ; leur conception demande un travail
                d'équipe pour le développement et parfois le
                maintient du logiciel, du jeu en lui-même ou de
                la machine et de l'interface qui permet d'y jouer.
                Puis vient la phase de fabrication : les
                équipements numériques nécessitent
                l'exploitation d'un nombre impressionnant de
                ressources naturelles notamment de terres rares
                dans des mines, ce qui demande de l'énergie.
                C'est dans l'immense majorité des cas le pétrole
                qui est utilisé avec pour conséquence l'émission
                de dioxyde de carbone, l'un des principaux gaz à
                effet de serre. Un autre des effets néfastes de ces
                mines dont la plupart sont situées en Afrique et
                en Asie est la destruction d'écosystèmes souvent
                occupés par des forêts. Rappelons que les forêts,
                en plus d'être garantes de la biodiversité, sont des
                puits naturels de carbone absorbant le dioxyde
                de carbone et permettant donc de limiter les
                effets du réchauffement climatique.</p>
            <p>Puis vient la phase de l'assemblage, gourmande
                en énergie, et finalement du transport jusqu'aux
                points de vente, toutes deux responsables de gaz
                à effet de serre supplémentaires. Avant même
                d'avoir été déballée par l'utilisateur, une console
                est donc responsable de la majorité de son
                impact écologique dans la totalité de son cycle de
                vie.</p><br>
            <a id="img" href="../assets/image/img_article/ges-flickr.webp"><img id="img" src="../assets/image/img_article/ges-flickr.webp"></a><br>
            <h3>Phase d'utilisation</h3><br>
            <p>La phase d'utilisation englobe la consommation
                électrique des appareils en utilisation ou en
                veille, mais également la consommation des data
                center dans le cas spécifique du cloud gaming (en
                français jeu à la demande), technique de jeu
                permettant de jouer peu importe l'endroit
                pourvu d'avoir une connexion internet suffisante
                ainsi que l'énergie nécessaire à la connexion
                internet. L'émission de gaz à effets de serre issus
                de cette phase dépendra pour beaucoup de la
                provenance de l'électricité utilisée.</p>
            Dans le cas d'un pays tel que la France dans
            laquelle la majorité de l'électricité provient de
            l'énergie nucléaire dont l'impact carbone est
            faible, le poids de la phase d'utilisation sera
            modéré. En revanche, si l'électricité est issue de
            sources d'énergie non-renouvelable telles que le
            gaz ou le charbon, son impact peut vite grimper.</p>
            La consommation des appareils varie
            grandement ; par exemple selon une étude de
            l'ADEME deux consoles et demi utilisent en
            moyenne autant d'énergie qu'un ordinateur et
            demi ou qu'un seul écran plat de 50 pouces.</p>
            Les smartphones qui rappelons-le servent
            également de support pour joueur aux jeux vidéo
            ont une consommation d'énergie très faible
            comparée aux autres appareils. Naturellement
            plus un écran est grand plus sa consommation
            d'énergie aura tendance à augmenter. Mais la
            phase d'utilisation comprend aussi
            paradoxalement l'énergie consommée par les
            appareils en mode veille qui est responsable
            selon une étude de 2017 de 46 pourcent du total
            de l'électricité consommée par les consoles aux
            Etats-Unis. Selon cette même étude, en
            Californie, la consommation d'énergie des PC est
            l'une des plus importantes des ménages.</p>
            Si avec le temps les consoles sont généralement
            devenues plus efficientes énergétiquement, ce
            n'est en effet pas le cas des PC gamers dont la
            consommation électrique a augmenté en même
            temps que la puissance. Le PC gamer moyen
            consomme en moyenne 1400 kWh par an soit
            l'équivalent de dix consoles ou encore de trois
            réfrigérateurs ou 6 PC normaux ! Alors que seuls
            2,5 % des PC en 2012 étaient des PC gamers, ils
            ont consommé dans le même temps 20 % du
            total de l'électricité consommée par l'ensemble
            du parc de PC !</p><br>
            <h3>Le dématérialisé</h3><br>
            <p>Depuis quelques années, on assiste à une
                explosion de l'utilisation du dématérialisé qui
                consiste à supprimer le besoin pour le joueur de
                recourir à un support physique pour faire l'achat
                de jeux. Face aux pollutions causées par la
                fabrication des équipements et à leur transport,
                on pourrait penser que le dématérialisé ou le
                cloud gaming puisse en constituer une
                alternative durable et écologique ; toutefois, il
                n'en est rien.</p>
            <p>Le dématérialisé nécessite en réalité d'immenses
                data center très énergivores et coûteux en
                ressources. À l'heure actuelle des études
                complètes manquent pour déterminer si le
                dématérialisé a un impact sur l'environnement
                moins marqué que les supports physiques des
                études partielles semblent infirmer cette
                hypothèse.</p><br>
