        <h2 id="IX">IX - Décroissance et jeux vidéo</h2><br>
        <p id="pp">Notre monde traverse aujourd’hui une crise majeure. Nous sommes face à un problème nouveau,
            que nous n’avons jamais eu à affronter avant et qui met toute notre espèce en danger.</p><br>

        <p>Notre monde va décroître dans les prochaines décennies. Dans toutes ces crises que nous affrontons, le
            réchauffement climatique n’est en vérité qu’une part du problème.
            Globalement, nous avons construit notre confort de vie occidentale sur une dette écologique immense.
            Une dette dont nous commençons à peine à payer une partie des conséquences, et nous ne passerons pas à
            travers du reste.</p><br>
        <h3>Des alertes ignorées</h3><br>
        <p>L’environnement dans lequel nous vivons
            devient peu à peu hostile aux modes de vies
            humains qui s’étaient développés jusqu’à présent.
            Les années à venir seront marquées par une
            multiplication des crises écologiques,
            économiques et sociales. C’est du moins ce que
            promet le rapport Meadows du MIT depuis les
            années 70, dont les projections rationalistes ne
            cessent d’être confirmées par d’autres études plus
            récentes.</p>
        Les mouvements de la décroissance et de la
        transition ont essayé d’alerter et de sensibiliser
        depuis de nombreuses années à propos de la
        nécessité de réorganiser la société autour d’un
        modèle plus solidaire, local et moins énergivore.
        En dépit d’un mouvement global gagnant
        toujours plus de terrain, les arguments de ses
        principaux défenseurs ont été ignoré, mais
        également parce que les institutions dominantes
        et les personnes qui les composent n’ont que peu
        d’intérêt à voir changer les choses.</p><br>
        <h3>Décroissance et collapsologie</h3><br>
        <p>Dans les discussions les plus récentes, Pablo
            Servigne et Raphaël Stevens, auteurs de
            Comment tout peut s’effondrer, ont cependant
            grandement contribué à légitimer la prise au
            sérieux de ces sujets. En dissociant les aspects
            scientifiques des aspects émotionnels pour mieux
            les articuler entre eux autour de la notion de
            Collapsologie (courant de pensée qui étudie les
            risques d'un effondrement de la civilisation
            industrielle et ce qui pourrait succéder à la
            société actuelle). Les deux auteurs ont réussi à
            vulgariser une pensée systémique de
            l’effondrement.</p>
        Là où le dogme du progrès fait de « l’innovation
        sociale et technologique » occultant
        complètement les conséquences matérielles et
        physiques du développement des sociétés, ils
        démontrent, des centaines de travaux
        scientifiques issus d’instances diverses à l’appui,
        que dans une société aussi complexe et
        interconnectée que la nôtre, les pressions sur les
        différents systèmes desquels nous dépendons,
        placent l’humanité face à un effondrement
        généralisé, quasi inéluctable.</p>
        Dans ce contexte, c’est bien un recul important
        du confort matériel et de la disponibilité des
        matières premières qui risquent d’être le nouveau
        quotidien des futures générations et le fil
        conducteur du bouleversement radical de la
        société. Demain, les technologies énergivores
        desquelles nous dépendons pourraient bien être
        notre plus grand ennemi, notre principale
        faiblesse.</p>
        En dépit de la difficulté de savoir à quoi
        ressembleront exactement les années à venir, peu
        de doute que les communautés locales
        autonomes seront les mieux préparées, là où les
        citoyens s’organisent aujourd’hui de manière
        collective pour vivre autrement, ensemble. Il n’y
        a donc plus de temps à perdre pour organiser
        collectivement une certaine forme de
        décroissance locale, développer les
        circuits-courts, penser une économie circulaire
        vraiment effective, cesser de soutenir les activités
        les plus polluantes, réapprendre à vivre
        décemment en toute simplicité et tant d’autres
        choses qui ne feront malheureusement jamais
        l’objet d’une page publicitaire.</p><br>
